# List Index
my_list = ['a', 't', 'o', 'z', 'k', 'o', 'w', 'l', 'e', 'd', 'g', 'e']
print(my_list[0])

# Nexted indexing
n_list = ["Happy", [2, 0, 1, 5]]
print(n_list[0][1])
print(n_list[1][3])

# Negative indexing
my_list = ['a', 't', 'o', 'z', 'k']
print(my_list[-1])
print(my_list[-5])

# Slice lists in Python
my_list = ['a', 't', 'o', 'z', 'k', 'o', 'w', 'l', 'e', 'd', 'g', 'e']
print(my_list[2:5])
print(my_list[:-5])
print(my_list[5:])
print(my_list[:])